#!/usr/bin/env python3
"""
send_email.py - Institutional Earnings Risk Report
Renders analysis_raw.json from the modern analysis_engine pipeline.
Uses .env for Gmail credentials.
"""

import json
import smtplib
import os
from datetime import datetime
from email.mime.text import MIMEText
from email.mime.multipart import MIMEMultipart
from pathlib import Path
from dotenv import load_dotenv

load_dotenv()

WORKSPACE = Path.home() / ".openclaw" / "workspace"
DATA_DIR = WORKSPACE / "data"
INPUT_FILE = DATA_DIR / "analysis" / "analysis_raw.json"
LOG_FILE = DATA_DIR / "logs" / "pipeline.log"


def log(msg):
    timestamp = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    with open(LOG_FILE, "a") as f:
        f.write(f"[{timestamp}] EMAIL: {msg}\n")
    print(f"[{timestamp}] EMAIL: {msg}")


def load_analysis():
    if not INPUT_FILE.exists():
        log(f"Missing file: {INPUT_FILE}")
        return [], {}
    with open(INPUT_FILE) as f:
        data = json.load(f)
    
    # Handle both old format (array) and new format (object with metadata)
    if isinstance(data, dict) and "candidates" in data:
        return data.get("candidates", []), data.get("metadata", {})
    return data, {}


def format_percent(value):
    """Format decimal probability as percentage."""
    if value is None:
        return "-"
    return f"{value * 100:.0f}%"


def format_move(value):
    """Format move percentage."""
    if value is None:
        return "-"
    return f"{value:+.1f}%"


